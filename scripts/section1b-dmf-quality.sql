-- ============================================
-- 7. カスタムDMF: 参照整合性チェック
-- ============================================

-- システムDMFにはない「参照整合性」チェックはカスタムDMFで実現
CREATE OR REPLACE DATA METRIC FUNCTION
  SECURITIES_WORKSHOP.RAW_DATA.DMF_FK_CUSTOMER_EXISTS(
    ARG_T TABLE(CUSTOMER_ID VARCHAR)
  )
  RETURNS NUMBER
  AS
  $$
    SELECT COUNT(*)
    FROM ARG_T
    WHERE CUSTOMER_ID NOT IN (
      SELECT CUSTOMER_ID FROM SECURITIES_WORKSHOP.RAW_DATA.CUSTOMERS
    )
  $$;

-- HOLDINGS テーブルに紐付け
ALTER TABLE SECURITIES_WORKSHOP.RAW_DATA.HOLDINGS
  ADD DATA METRIC FUNCTION
    SECURITIES_WORKSHOP.RAW_DATA.DMF_FK_CUSTOMER_EXISTS ON (CUSTOMER_ID);

-- DAILY_TRADES テーブルにも紐付け
ALTER TABLE SECURITIES_WORKSHOP.RAW_DATA.DAILY_TRADES
  ADD DATA METRIC FUNCTION
    SECURITIES_WORKSHOP.RAW_DATA.DMF_FK_CUSTOMER_EXISTS ON (CUSTOMER_ID);

-- アドホック実行で動作確認
SELECT SECURITIES_WORKSHOP.RAW_DATA.DMF_FK_CUSTOMER_EXISTS(
  SELECT CUSTOMER_ID FROM SECURITIES_WORKSHOP.RAW_DATA.HOLDINGS
);

SELECT SECURITIES_WORKSHOP.RAW_DATA.DMF_FK_CUSTOMER_EXISTS(
  SELECT CUSTOMER_ID FROM SECURITIES_WORKSHOP.RAW_DATA.DAILY_TRADES
);

