/*
このセクションのポイント:

データメトリック関数（DMF）: Snowflake組み込みの品質チェック機構
システムDMF: NULL_COUNT, DUPLICATE_COUNT, UNIQUE_COUNT, ROW_COUNT, FRESHNESS
スケジュール実行: テーブル単位でDMFを紐付け、定期的に自動計測
結果の確認: SNOWFLAKE.LOCAL.DATA_QUALITY_MONITORING_RESULTS() で計測値を取得

※ Enterprise Edition 以上が必要です
*/
USE ROLE MU;
USE SCHEMA SECURITIES_WORKSHOP.RAW_DATA;

-- ============================================
-- 1. DMFの概要
-- ============================================
-- section1-copy_into.sql のセクション6〜8では、手動SQLで品質チェックを行いました。
-- DMFを使うと、同様のチェックをテーブルに紐付けて自動的・継続的に実行できます。
--
-- 手動SQL品質チェック:
--   - 毎回SQLを実行する必要がある
--   - 結果は自前のログテーブルに記録
--
-- DMF（データメトリック関数）:
--   - テーブルに紐付けてスケジュール実行
--   - 結果はSnowflakeが自動管理（トレンド分析可能）
--   - システムDMF（組み込み）とカスタムDMF（自作）がある

-- ============================================
-- 2. システムDMFの一覧確認
-- ============================================

-- Snowflakeが提供するシステムDMF一覧
-- SNOWFLAKE.CORE スキーマに格納されています
SHOW DATA METRIC FUNCTIONS IN SCHEMA SNOWFLAKE.CORE;

-- ============================================
-- 3. DMFをアドホック（単発）で実行してみる
-- ============================================

-- 顧客テーブルの CUSTOMER_ID のNULL件数
SELECT SNOWFLAKE.CORE.NULL_COUNT(
  SELECT CUSTOMER_ID FROM SECURITIES_WORKSHOP.RAW_DATA.CUSTOMERS
);

-- 顧客テーブルの CUSTOMER_ID のユニーク件数
SELECT SNOWFLAKE.CORE.UNIQUE_COUNT(
  SELECT CUSTOMER_ID FROM SECURITIES_WORKSHOP.RAW_DATA.CUSTOMERS
);

-- 顧客テーブルの CUSTOMER_ID の重複件数
SELECT SNOWFLAKE.CORE.DUPLICATE_COUNT(
  SELECT CUSTOMER_ID FROM SECURITIES_WORKSHOP.RAW_DATA.CUSTOMERS
);

-- 市場株価テーブルのフレッシュネス（最終更新からの経過時間）
SELECT SNOWFLAKE.CORE.FRESHNESS(
  SELECT LOAD_TIMESTAMP FROM SECURITIES_WORKSHOP.RAW_DATA.MARKET_PRICES
);

-- ============================================
